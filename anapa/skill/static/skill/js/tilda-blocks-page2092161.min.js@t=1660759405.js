function t708__scroll(target) {
    var duration = 400;
    var start = (window.pageYOffset || document.documentElement.scrollTop) - (document.documentElement.clientTop || 0);
    var change = target - start;
    var currentTime = 0;
    var increment = 16;
    document.body.setAttribute('data-scrollable', 'true');

    function t708__easeInOutCubic(currentTime) { if ((currentTime /= duration / 2) < 1) { return change / 2 * currentTime * currentTime * currentTime + start } else { return change / 2 * ((currentTime -= 2) * currentTime * currentTime + 2) + start } }

    function t708__animateScroll() { currentTime += increment;
        window.scrollTo(0, t708__easeInOutCubic(currentTime)); if (currentTime < duration) { setTimeout(t708__animateScroll, increment) } else { document.body.removeAttribute('data-scrollable') } }
    t708__animateScroll()
}

function t708__triggerEvent(el, eventName) {
    var event;
    if (typeof window.CustomEvent === 'function') { event = new CustomEvent(eventName) } else if (document.createEvent) { event = document.createEvent('HTMLEvents');
        event.initEvent(eventName, !0, !1) } else if (document.createEventObject) { event = document.createEventObject();
        event.eventType = eventName }
    event.eventName = eventName;
    if (el.dispatchEvent) { el.dispatchEvent(event) } else if (el.fireEvent) { el.fireEvent('on' + event.eventType, event) } else if (el[eventName]) { el[eventName]() } else if (el['on' + eventName]) { el['on' + eventName]() }
    if (t708__checkJqueryEvent(el, eventName)) { $(el).trigger(eventName) }
}

function t708__checkJqueryEvent(element, eventName) {
    var events = $._data(element, 'events') || !1;
    var isEvent = !1;
    if (events) { for (var key in events) { if (key === eventName) { isEvent = !0; break } } }
    return isEvent
}


